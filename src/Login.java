    private void usernameKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_usernameKeyTyped
        char c=evt.getKeyChar();                                    // FOR CARD NUMBER very useful thing to learn field cannot exeed 16 digits or be a char 
        String Fieldlength=username.getText();                            // if so you will hear a BEEP
        if(Fieldlength.length()>15){
            if(c==KeyEvent.VK_BACK_SPACE){}
            else{getToolkit().beep();}
            evt.consume();
        }
    }//GEN-LAST:event_usernameKeyTyped

    private void passwordKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_passwordKeyTyped
        char c=evt.getKeyChar();                                    // FOR CARD NUMBER very useful thing to learn field cannot exeed 16 digits or be a char 
        String Fieldlength=password.getText();                            // if so you will hear a BEEP
        if(!(Character.isDigit(c))||Fieldlength.length()>15){
            if(c==KeyEvent.VK_BACK_SPACE){}
            else{getToolkit().beep();}
            evt.consume();
        }
    }//GEN-LAST:event_passwordKeyTyped

