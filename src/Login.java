    private void jButtonLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonLoginActionPerformed
        String sql="Select * from Account where Username=? and Password=?";
        try{                                                    /*SQL code to check wheather we inputted correct data or not*/
            pst=conn.prepareStatement(sql);
            pst.setString(1, username.getText().toLowerCase());//paste result instead of ? lowercase function, 
                                                                    //we check only for lowercase inputs, bcs it is easier for user
            pst.setString(2, password.getText().toLowerCase());//paste result instead of ?
            rs=pst.executeQuery();
            if(rs.next()){
                rs.close();
                pst.close();
                //new HomePage().userName=username.getText();
                setVisible(false);/*If everyThing is fine go to Loading page*/
                Loading ob=new Loading();
                ob.setUpLoading();/*user defined func inside Loading class*/
                ob.setVisible(true);
                Loading.userName1=username.getText().toLowerCase();// pass it to loading, and loading passes to HomePage
            }
            else{JOptionPane.showMessageDialog(null, "Incorrect Username or Password");}
        }catch(SQLException e){
            JOptionPane.showMessageDialog(null, e);
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }finally{     // anyway will be done
            try{
                rs.close();
                pst.close();
            }
            catch(Exception e){
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }//GEN-LAST:event_jButtonLoginActionPerformed

